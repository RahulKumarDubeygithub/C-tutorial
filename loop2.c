#include<stdio.h>
int main()
{
    for( int i=0;i<=10;i++)
    {
        printf("%d\n",i);
    }
    for( float i=0;i<=10;i++)
    {
        printf("%f\n",i);
    }
    for( double i=0;i<=10;i++)
    {
        printf("%lf\n",i);
    }
    for( char i=65;i<=70;i++)
    {
        printf("%c\n",i);
    }
    
}